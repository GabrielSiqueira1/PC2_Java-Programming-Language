    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField2.getText().isEmpty() == true && jTextField1.getText().isEmpty() == false){
            if(estoque.listaCategoria(jTextField1.getText()).isEmpty()){
                JOptionPane.showMessageDialog(null,"Não há filmes dessa categoria");
            }
            else{
                JOptionPane.showMessageDialog(null, estoque.listaCategoria(jTextField1.getText()));
            }
        }
        else if(jTextField2.getText().isEmpty() == false){
            Filme f = estoque.verificaExistencia(jTextField2.getText());
            if(f != null){
                JOptionPane.showMessageDialog(null, estoque.exibeFilme(f));
            }
            else{
                JOptionPane.showMessageDialog(null, "O filme não está no catálogo");
            }           
        }
        else{
            JOptionPane.showMessageDialog(null, "Insira um atributo para pesquisar no catálogo");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Insira o título do filme a ser alugado");
        }
        else{
            Filme f = estoque.verificaExistencia(jTextField2.getText());
            if(f != null){
                if(estoque.verificaDiponibilidade(f) > 0){
                    JOptionPane.showMessageDialog(null, "O filme "+f.getTitulo()+" foi alugado");
                    estoque.getEstoque().put(f, estoque.getEstoque().get(f)-1);
                }
                else if(estoque.verificaDiponibilidade(f) == 0){
                    JOptionPane.showMessageDialog(null, "O filme "+f.getTitulo()+" não possui cópias disponíveis");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "O filme não está no catálogo");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

