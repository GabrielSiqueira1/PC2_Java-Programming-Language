    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Insira o título do filme a ser devolvido");
        }
        else{
            Filme f = estoque.verificaExistencia(jTextField2.getText());
            if(f != null){
                if(estoque.verificaDiponibilidade(f) < 3){
                    JOptionPane.showMessageDialog(null, "O filme "+f.getTitulo()+" foi devolvido");
                    estoque.getEstoque().put(f, estoque.getEstoque().get(f)+1);
                }
                else if(estoque.verificaDiponibilidade(f) == 3){
                    JOptionPane.showMessageDialog(null, "O filme "+f.getTitulo()+" ainda não foi alugado");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "O filme não pertence ao catálogo");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

